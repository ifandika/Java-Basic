package java.code;

public class PrintAE {
    
    public static void main(String[] args) {
        
        // .println = print dan menambahkan baris baru
        System.out.println("Baris 1: println");
        // .print = hanya print saja, tidak menambahkan baris baru
        System.out.print("Baris 2: print");
        System.out.print(" Baris 3: print");
        
        // \n = new line, menambahkan enter secara manual
        System.out.print("\nBaris 4: print dengan enter \n");
        
        // .printf = 
        System.out.printf("Baris 5: 1 + 1 = %d", 2);
        System.out.print("\n");
        
    }
}