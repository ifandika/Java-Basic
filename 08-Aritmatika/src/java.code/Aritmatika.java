package java.code;

public class Aritmatika {
    
    public static void main(String[] args) {
        
        // aritmatika
        
        int x = 10;
        int y = 3;
        int hasil;
        
        // penjumlahan +
        hasil = x + y;
        System.out.printf("%d + %d = %d\n", x, y, hasil);
        
        // pengurangan -
        hasil = x - y;
        System.out.printf("%d - %d = %d\n", x, y, hasil);
        
        // perkalian *
        hasil = x * y;
        System.out.printf("%d * %d = %d\n", x, y, hasil);
        
        // pembagian /
        hasil = x / y;
        System.out.printf("%d / %d = %d\n", x, y, hasil);
    
        // modulu %
        hasil = x % y;
        System.out.println(x + " % " + y + " = " + hasil);
        
    }
}
