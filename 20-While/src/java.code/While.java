package java.code;

public class WhileLoop {
    
    public static void main(String[] args) {
        
        // while loop = perulangan
        
        int days = 1;
        
        while(days <= 7) {
            
            System.out.println("Days " + days);
            
            // menambahkan expresi, jika tidak maka akan looping tanpa batas
            days++;
        }
        
    }
}
