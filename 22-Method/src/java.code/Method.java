package java.code;

public class Method {
    
    public static void main(String[] args) {
        
        // method / function = sebuah block / fungsi yang kita panggil akan menampilkan hasil
        // - dengan return -> 
        // - tanpa return -> void
        
        // memanggil method
        printHelloWord();
    }
    
    // membuat method ( void/tanpa kembalian )
    static void printHelloWord() {
      
      System.out.println("Hello Word");
      
    }
}
